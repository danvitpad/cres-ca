/** Resolves the most specific help entry for a given pathname. Returns null
 *  if no match found (means: don't render the help button on this route). */
export function resolvePageHelp(pathname: string): PageHelp | null {
  // Strip locale prefix /ru, /uk, /en → '/'
  const stripped = pathname.replace(/^\/[a-z]{2}(?=\/|$)/, '') || '/';
  const matches = HELP.filter((h) => stripped.startsWith(h.match));
  if (!matches.length) return null;
  matches.sort((a, b) => b.match.length - a.match.length);
  return matches[0].help;
}
