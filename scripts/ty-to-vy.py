# Эти подстроки в путях — пропускаем (тесты, миграции, документы).
SKIP_DIR_KEYWORDS = ['node_modules', '.next', '__tests__', '.test.', '.spec.']

def should_skip(path):
    norm = path.replace('\\', '/')
    return any(k in norm for k in SKIP_DIR_KEYWORDS)


def process_file(path):
    try:
        with open(path, 'r', encoding='utf-8') as f:
            text = f.read()
    except Exception as e:
        print(f'SKIP (read error): {path} ({e})', file=sys.stderr)
        return 0

    original = text
    for pattern, repl in REPLACEMENTS:
        text = re.sub(pattern, repl, text)

    if text != original:
        with open(path, 'w', encoding='utf-8') as f:
            f.write(text)
        # Считаем сколько строк изменено
        changed = sum(1 for a, b in zip(original.splitlines(), text.splitlines()) if a != b)
        return changed
    return 0


def main():
    total_files = 0
    total_changes = 0
    for dirpath, _, filenames in os.walk(ROOT):
        if should_skip(dirpath):
            continue
        for fn in filenames:
            if not (fn.endswith('.ts') or fn.endswith('.tsx') or fn.endswith('.json')):
                continue
            path = os.path.join(dirpath, fn)
            if should_skip(path):
                continue
            changed = process_file(path)
            if changed:
                total_files += 1
                total_changes += changed
                rel = os.path.relpath(path, ROOT)
                print(f'  {changed:3d} lines  {rel}')
    print(f'\n=== Done: {total_files} files, {total_changes} lines changed ===')


if __name__ == '__main__':
    main()
